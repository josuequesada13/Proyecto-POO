    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        Persona persona = familiaActual.buscarPersona((String)jComboMiembros.getSelectedItem());
        if(jTextTrabajo.getText().equals("")){
            JOptionPane.showMessageDialog(rootPane, "Por favor escriba un trabajo valido.");
        }else{
            persona.anadeTrabajo(jTextTrabajo.getText());
            jTextTrabajo.setText("");
            this.setVisible(false);
        }
    }//GEN-LAST:event_btnAnadirActionPerformed

    
    public void setMiembros(){
        if(familiaActual.getMiembrosFamilia() == null){
            jComboMiembros.setEnabled(false);
        }else{
            jComboMiembros.setEnabled(true);
            String[] miembros = new String[familiaActual.getMiembrosFamilia().size()]; 
            for(int i = 0; i < familiaActual.getMiembrosFamilia().size(); i++){
                miembros[i] = familiaActual.getMiembrosFamilia().get(i).getNombre();
            }
            DefaultComboBoxModel model = new DefaultComboBoxModel(miembros);
            jComboMiembros.setModel(model);
        }  
    }
